def build_system_prompt(scenario_key: str, persona_key: str) -> str:
    """시나리오 + 페르소나 조합으로 시스템 프롬프트 생성"""
    scenario = SCENARIOS[scenario_key]
    persona = PERSONAS[persona_key]

    query = f"{scenario['title']} {scenario['situation']}"
    docs = retrieve(query, top_k=3)
    domain_context = format_context(docs)

    questions_formatted = "\n".join(
        f"  - {q}" for q in scenario["key_questions"]
    )
    traps_formatted = "\n".join(
        f"  - {t}" for t in scenario["traps"]
    )

    return SYSTEM_TEMPLATE.format(
        customer_role=scenario["customer_role"],
        situation=scenario["situation"],
        persona_style=persona["style"],
        questions_formatted=questions_formatted,
        traps_formatted=traps_formatted,
        domain_context=domain_context,
    )


def get_ai_response(system_prompt: str, chat_history: list[dict]) -> str:
    """대화 히스토리 기반 AI 고객 응답 생성"""
    conversation = ""
    for msg in chat_history:
        role = "사업담당" if msg["role"] == "user" else "고객(나)"
        conversation += f"{role}: {msg['content']}\n\n"

    prompt = f"지금까지의 대화:\n{conversation}\n고객(나)으로서 다음 응답을 하세요."

    return call_claude(prompt, system_prompt=system_prompt)


def get_opening_message(system_prompt: str) -> str:
    """AI 고객의 첫 인사 생성"""
    return call_claude("(미팅 시작)", system_prompt=system_prompt)
