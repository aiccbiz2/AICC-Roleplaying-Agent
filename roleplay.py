def _recover_partial_json(text: str) -> dict:
    """불완전한 JSON에서 response 필드만이라도 추출 (말풍선에 raw JSON 노출 방지)"""
    import re
    result = {"response": "", "choices": [], "correct_index": 0, "explanation": ""}

    # "response":"..." 패턴 추출
    m = re.search(r'"response"\s*:\s*"((?:[^"\\]|\\.)*)"', text)
    if m:
        result["response"] = m.group(1)
    else:
        # JSON이 아닌 경우 텍스트 그대로 (단, JSON 특수문자 제거)
        cleaned = re.sub(r'[{}\[\]":]', '', text).strip()
        result["response"] = cleaned[:200] if cleaned else "(응답 생성에 실패했습니다. 다시 시도해주세요.)"

    # choices 부분 추출 시도
    choices_match = re.findall(r'"([^"]{10,})"', text)
    # response 이후의 문자열들 중 선택지로 보이는 것들
    if len(choices_match) > 1:
        # 첫 번째는 response이므로 제외, 나머지 중 최대 4개
        candidates = [c for c in choices_match[1:] if len(c) > 10 and not c.startswith("{")]
        result["choices"] = candidates[:4]

    return result


def _parse_mc_response(text: str) -> dict:
    """객관식 JSON 응답 파싱"""
    import json
    text = text.strip()
    # 마크다운 코드블록 제거 (```json ... ``` 등)
    while text.startswith("```"):
        first_newline = text.find("\n")
        if first_newline > 0:
            text = text[first_newline + 1:]
        if text.endswith("```"):
            text = text[:-3]
        text = text.strip()
    # JSON 파싱 시도
    try:
        data = json.loads(text)
    except (json.JSONDecodeError, ValueError):
        # 텍스트에서 JSON 객체 추출 시도
        try:
            start = text.find("{")
            end = text.rfind("}") + 1
            if start >= 0 and end > start:
                data = json.loads(text[start:end])
            else:
                # 불완전 JSON에서 response 필드만 추출 시도
                return _recover_partial_json(text)
        except (json.JSONDecodeError, ValueError):
            # 불완전 JSON에서 response 필드만 추출 시도
            return _recover_partial_json(text)

    # 후처리: 물음표 제거 및 4개로 제한
    choices = data.get("choices", [])
    choices = [c.rstrip("?").rstrip() if c.endswith("?") else c for c in choices]
    if len(choices) > 4:
        choices = choices[:4]
    data["choices"] = choices

    # correct → correct_index 변환 (A=0, B=1, C=2, D=3)
    letter_map = {"A": 0, "B": 1, "C": 2, "D": 3}
    correct_letter = str(data.get("correct", "A")).strip().upper()
    ci = letter_map.get(correct_letter)
    if ci is None:
        # correct_index 직접 지정된 경우 (fallback)
        try:
            ci = int(data.get("correct_index", 0))
        except (TypeError, ValueError):
            ci = 0
    if ci < 0 or ci >= max(len(choices), 1):
        ci = 0
    data["correct_index"] = ci

    # explanation 기본값
    if "explanation" not in data:
        data["explanation"] = ""

    return data


